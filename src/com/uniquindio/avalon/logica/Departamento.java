package com.uniquindio.avalon.logica;

public class Departamento {

	private String nombre;
	private int codigo;
	
	public Departamento(){
		
	}
	
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public int getCodigo() {
		return codigo;
	}
	public void setCodigo(int codigo) {
		this.codigo = codigo;
	}
}
